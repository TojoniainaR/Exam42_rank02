#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nbr)
{
	if (nbr >= 10)
		ft_putnbr(nbr / 10);
	ft_putchar(nbr % 10 + '0');
}

int	main(int ac, char *av[])
{
	if (av)
		ft_putnbr(ac - 1);
	ft_putchar('\n');
	return (0);
}
